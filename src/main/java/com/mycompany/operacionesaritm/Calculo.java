public class Calculo {
    
    double x;
    double y;
    double r;

    public double suma (double x, double y){
        r = x + y;
        return r;
    }
    
    public double resta (double x, double y){
        r = x - y;
        return r;
    }
    
    public double multiplicacion (double x, double y){
        r = x * y;
        return r;
    }
    
    public double division (double x, double y){
        r = x/y;
        return r;
    }
}
